import math

# trigonometria
resultado = math.tan(45)
print(resultado)

# logaritmo
resultado = math.log(25, 5)
print(resultado)

# pi
resultado = math.pi
print(resultado)

# redondea hacia arriba
resultado = math.ceil(89.6545)
print(resultado)

# redondea hacia abajo
resultado = math.floor(89.6545)
print(resultado)
